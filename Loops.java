

public class Loops{
    public static void main(String[] args) {
        // for loop
        // while loop
        // do while loop

        System.out.println("for loop:");
        for(int i=0; i<=100; i++){
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();
        System.out.println("while loop:");
        int i=0;
        while(i<=100)
        {
            System.out.print(i);
            System.out.print(" ");
            i++;
        }
    }
}